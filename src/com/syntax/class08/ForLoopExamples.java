package com.syntax.class08;

public class ForLoopExamples {

	public static void main(String[] args) {
		// i need to print num from 1 to 90
		
		for(int i= 1; i<=90; i++) {
			System.out.print(i+" ");
		}
		System.out.println();
		
		/* stsrt point
		 * end point
		 * increment/decrement
		 */
		
		// i need num from 60 to 10
		
		for(int a= 60; a>=10; a--) {
			System.out.print(a+" ");
		}
		System.out.println();
		
		//what is the output
		for(int b=5; b<=40; b+=5) {
			System.out.print(b+" ");
		}
		System.out.println();
		
	}

}
